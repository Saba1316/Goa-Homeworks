// Hoisting is a concept in JavaScript (and other programming languages) 
// related to how variables and function declarations are moved to the top of their containing scope during the compilation phase, before the code is executed.


// Summary:
// var declarations are hoisted and initialized to undefined.
// let and const declarations are hoisted but placed in the "temporal dead zone."
// Function declarations are hoisted with their implementation.
// Function expressions and class declarations are not hoisted in the same way.
