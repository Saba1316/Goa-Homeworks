// Explain how the ternary and && operators work

// Ternary Operator (? :)
// The ternary operator is a shorthand for an if-else statement. It evaluates a condition and returns one value if the condition is true, 
// and another value if the condition is false. It follows this structure:

// Condition: The expression that is evaluated.
// True Expression: The result returned if the condition is true.
// False Expression: The result returned if the condition is false.
// It is a compact way of writing conditional logic in a single line.


// Logical AND (&&) Operator
// The logical AND operator checks if both conditions are true. If both conditions are true, it returns the second operand. 
// If either condition is false, it returns the first falsy value encountered. It can also be used for short-circuiting, where the second condition isn't evaluated 
// if the first condition is false. This makes the && operator useful for conditional execution and evaluations.

