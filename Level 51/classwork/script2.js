//  Question: Do we create variables statically or dynamically in JavaScript? how do we get?

//  In JavaScript, variables are created dynamically, not statically. This means that you do not need to specify the type of a variable ahead of time. 
//  JavaScript uses dynamic typing, where the type of a variable is determined at runtime based on the value assigned to it. 
//  You can assign any type of value to a variable, and its type can change as the program runs.