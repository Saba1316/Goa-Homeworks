# Explain the difference between _ prefix and __ prefix and which one should be used when in python


# In Python, the single underscore (_) and double underscore (__) prefixes are used 
# as conventions to indicate the intended visibility and privacy of variables and methods, but they are not enforced by the Python language itself.
# These are purely conventions, and Python relies on developers to respect these conventions rather than enforcing strict access control like some other languages do.