// Use indexing on all three arrays

// Creating an array with a length of 5, but no initial values
let arrayWithLength = new Array(5);

// Using indexing to assign values to each index
arrayWithLength[0] = 'apple';
arrayWithLength[1] = 'banana';
arrayWithLength[2] = 'cherry';
arrayWithLength[3] = 'date';
arrayWithLength[4] = 'elderberry';

// Accessing and modifying elements using indexing
console.log(arrayWithLength[0]); // 'apple'
console.log(arrayWithLength[1]); // 'banana'
console.log(arrayWithLength[2]); // 'cherry'
console.log(arrayWithLength[3]); // 'date'
console.log(arrayWithLength[4]); // 'elderberry'

// Modifying an element at index 2
arrayWithLength[2] = 'grape';
console.log(arrayWithLength[2]); // 'grape'
