// Create an Array with the new keyword, write in the Array function how many elements you want, and then assign the indices to their values ​​separately


// Step 1: Create an array with 5 elements (empty slots initially)
let myArray = new Array(5);

// Step 2: Assign values to specific indices
myArray[0] = 'apple';
myArray[1] = 'banana';
myArray[2] = 'cherry';
myArray[3] = 'date';
myArray[4] = 'elderberry';

// Step 3: Print the array to verify the values
console.log(myArray); // Output: [ 'apple', 'banana', 'cherry', 'date', 'elderberry' ]
