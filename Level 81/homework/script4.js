// Explain the general idea of ​​classes, what we use them for and why

// In object-oriented programming (OOP), a class is a blueprint or template used to create objects. 
// An object is an instance of a class, and a class defines the properties (attributes) and methods (functions) that the objects created from it will have.

// You can think of a class as a blueprint for a house. The class defines how the house should be built 
// (the properties and methods), while each object (instance) of the class is an actual house built based on that blueprint.

// In programming, classes help organize code and model real-world entities by grouping related properties and behaviors.

// What We Use Classes For
// Modeling Real-World Entities:

// Once a class is defined, it can be used to create multiple objects, making the code reusable.
// This avoids redundancy. For example, a Dog class can be used to create many different dog objects, each with unique properties but all sharing common methods defined in the Dog class.
// Inheritance:

// One of the key features of classes is inheritance, where a class (child class) can inherit properties and methods from another class (parent class). 
// This promotes code reuse and reduces redundancy.
// For example, a Car class might inherit from a more general Vehicle class, and both would 
// share common methods like start() or stop(). The Car class can also have specific methods, like playMusic().