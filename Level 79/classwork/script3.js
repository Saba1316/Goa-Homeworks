// Explain how rest and spread work


// Rest Operator (...)
// The rest operator is used to collect multiple elements into a single array or object. It's often used in function parameters, array destructuring, and object destructuring.

// In function parameters, it collects all remaining arguments into an array.
// In destructuring, it collects the remaining values or properties into a new array or object.

// Spread Operator (...)
// The spread operator is used to expand elements of an array or object into individual elements. It's used for copying, merging, or adding new values to arrays or objects.

// In arrays, it spreads elements out to create a new array.
// In objects, it spreads properties out to create a new object or combine multiple objects.

// Rest: Collects multiple items into one array or object.
// Used in function parameters to gather arguments.
// Used in destructuring to collect remaining elements or properties.
// Spread: Expands an array or object into individual elements or properties.
// Used to copy arrays or objects.
// Used to combine multiple arrays or objects.
// Used to add new elements or properties to an existing array or object.

// Rest collects multiple values into an array or object.
// Spread expands values from arrays or objects into individual items.