// 3 Examples

let age = 20;
let status = age >= 18 ? "adult" : "minor";
console.log(status); // "adult"


let isLoggedIn = true;
let message = isLoggedIn ? "Welcome!" : "Please log in";
console.log(message); // "Welcome!"


let score = 75;
let grade = score > 90 ? "A" : score > 80 ? "B" : "C";
console.log(grade); // "C"
