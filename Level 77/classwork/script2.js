// Create 2 functions with the help of const keyword and arrow

// Example 1

// Using the const keyword and an arrow function to define a function for adding two numbers
const addNumbers = (a, b) => {
    return a + b;
};

console.log(addNumbers(3, 4));  // Output: 7


// Example 2

// Using the const keyword and an arrow function to define a function that greets a person
const greetPerson = (name) => {
    return `Hello, ${name}!`;
};

console.log(greetPerson('Alice'));  // Output: Hello, Alice!
