// Create 3 functions with the help of const keyword and arrow

// Example 1

const isEven = (num) => num % 2 === 0;

console.log(isEven(4)); // Output: true
console.log(isEven(7)); // Output: false

// Example 2

const greet = (name) => {
    return `Hello, ${name}!`;
};

console.log(greet('Saba')); // Output: Hello, Alice!

// Example 3


const multiply = (x, y) => x * y;

console.log(multiply(4, 5)); // Output: 20