# Task 3: Create a function that accepts first name, last name, age and academy as arguments. Say the following sentence: My name is ..., my surname is ..., I study in ....

def sentence(name, surname, age, academy):
    print(f"My name is {name}, my surname is {surname}, I am {age} years old, I study in {academy}")

sentence("Saba", "Giorgadze", 15, "GOA")